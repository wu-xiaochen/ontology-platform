def format_law_content(rule_id):
    """格式化Law内容"""
    if rule_id not in LAWS_CONTENT:
        return "N/A"
    
    law = LAWS_CONTENT[rule_id]
    content = f"规则: {law.get('statement', '')}\n"
    
    if law.get('conditions'):
        content += "条件:\n"
        for c in law['conditions']:
            content += f"  • {c}\n"
    
    if law.get('effects'):
        content += "效果:\n"
        for e in law['effects']:
            content += f"  • {e}\n"
    
    content += f"来源: {law.get('source', '')}"
    
    return content.strip()


def format_rule_content(rule_id):
    """格式化Rule内容"""
    if rule_id not in RULES_CONTENT:
        return "N/A"
    
    rule = RULES_CONTENT[rule_id]
    content = f"公式: {rule.get('formula', '')}\n"
    
    if rule.get('variables'):
        content += "变量:\n"
        for k, v in rule['variables'].items():
            content += f"  • {k}: {v}\n"
    
    content += f"来源: {rule.get('source', '')}"
    
    return content.strip()


